package main

import (
	"fmt"
)

func rob(nums []int) (total int) {
	lastTotal := 0
	for _, v := range nums {
		x := total
		if lastTotal+v > total {
			total = lastTotal + v
		}
		lastTotal = x
	}
	return
}

